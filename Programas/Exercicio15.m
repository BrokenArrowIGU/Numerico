%-------------------------------------------------------
Ea=10^-6;
%-------------------------------------------------------
f1=@(x) x(1)^2*cos(x(2)) + 10*x(1)*x(2)*x(3) - 7;
f2=@(x) x(2)^2*sin(x(2)) + x(3) + 20;
f3=@(x) 0.5*x(1) + 10*x(3)*exp(x(2)) - 2;
df1x= @(x) 2*x(1)*cos(x(2)) + 10*x(2)*x(3);
df1y= @(x) -x(1)^2*sin(x(2)) + 10*x(1)*x(3);
df1z= @(x) 10*x(1)*x(2);
df2x= @(x) 0;
df2y= @(x) x(2)*(2*sin(x(2)) + x(2)*cos(x(2)));
df2z= @(x) 1;
df3x= @(x) 0.5;
df3y= @(x) 10*x(3)*exp(x(2));
df3z= @(x) 10*exp(x(2));
%-------------------------------------------------------
J = @(x)[df1x(x) df1y(x) df1z(x); df2x(x) df2y(x) df2z(x); df3x(x) df3y(x) df3z(x)];
f = @(x)[f1(x);f2(x);f3(x)];
x0 = [1;1;1];
%-------------------------------------------------------
%nhm -> True - Modificado; False -> Completo
[x1,~,t1] = Newton_Raphsons(J,f,Ea,x0,false);
[x2,~,t2] = Newton_Raphsons(J,f,Ea,x0,true);